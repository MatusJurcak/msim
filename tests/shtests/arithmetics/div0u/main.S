#define ehalt .word 0x8200
#define cpu_dump .word 0x8201

.section .vectors, "a"
    .org 0x0
    .long _start         /* Power on reset - PC */

    .org 0x4
    .long 0xFFFF8000     /* Power on reset - SP */

.section .text
    .org 0x400
_start:
    sett
    cpu_dump
    // Clears the Q-bit, T-bit and the M-bit
    div0u
    cpu_dump
    ehalt